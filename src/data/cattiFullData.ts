export function buildCattiFullResult(answers: Record<string, CattiDim>): CattiResult {
  const count: Record<CattiDim, number> = { E: 0, I: 0, S: 0, N: 0, T: 0, F: 0, J: 0, P: 0 }
  Object.values(answers).forEach(dim => { count[dim]++ })

  const e = count.E >= count.I ? 'E' : 'I'
  const s = count.S >= count.N ? 'S' : 'N'
  const t = count.T >= count.F ? 'T' : 'F'
  const j = count.J >= count.P ? 'J' : 'P'
  const typeCode = e + s + t + j

  const data = CATTI_RESULTS_MAP[typeCode] || CATTI_RESULTS_MAP['INFP']
  return { type: typeCode, ...data }
}
